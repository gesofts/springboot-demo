public class Pivs {
    private int id;
    private String pivName;
    private String pivNo;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getPivName() {
        return pivName;
    }

    public void setPivName(String pivName) {
        this.pivName = pivName;
    }

    public String getPivNo() {
        return pivNo;
    }

    public void setPivNo(String pivNo) {
        this.pivNo = pivNo;
    }
}
